package uk.gov.hmcts.cp.subscription.mappers;

import org.junit.jupiter.api.Test;
import uk.gov.hmcts.cp.subscription.entities.ClientEventEntity;
import uk.gov.hmcts.cp.subscription.entities.EventTypeEntity;

import java.util.List;
import java.util.Set;
import java.util.UUID;

import static org.assertj.core.api.Assertions.assertThat;

class ClientEventMapperTest {

    private final ClientEventMapper clientEventMapper = new ClientEventMapperImpl();

    @Test
    void mapToClientEventEntity_should_map_subscription_id_and_event_type_to_entity() {
        // Given
        UUID subscriptionId = UUID.randomUUID();
        EventTypeEntity eventTypeEntity = EventTypeEntity.builder()
                .id(1L)
                .eventName("PRISON_COURT_REGISTER_GENERATED")
                .displayName("Prison court register")
                .category("REGISTER")
                .build();

        // When
        ClientEventEntity result = clientEventMapper.mapToClientEventEntity(subscriptionId, eventTypeEntity);

        // Then
        assertThat(result.getId()).isNull(); // ID should be ignored (auto-generated by DB)
        assertThat(result.getSubscriptionId()).isEqualTo(subscriptionId);
        assertThat(result.getEventTypeId()).isEqualTo(1L);
    }

    @Test
    void mapToClientEventEntities_should_map_subscription_id_and_event_types_list() {
        // Given
        UUID subscriptionId = UUID.randomUUID();
        Set<EventTypeEntity> eventTypeEntities = Set.of(
                EventTypeEntity.builder()
                        .id(1L)
                        .eventName("PRISON_COURT_REGISTER_GENERATED")
                        .displayName("Prison court register")
                        .category("REGISTER")
                        .build(),
                EventTypeEntity.builder()
                        .id(2L)
                        .eventName("WARRANT_SUPPLEMENT")
                        .displayName("Warrant Supplement")
                        .category("WARRANT")
                        .build()
        );

        // When
        List<ClientEventEntity> result = clientEventMapper.mapToClientEventEntityList(subscriptionId, eventTypeEntities);

        // Then
        assertThat(result).hasSize(2);

        assertThat(result).allMatch(entity -> entity.getSubscriptionId().equals(subscriptionId));
        assertThat(result).allMatch(entity -> entity.getId() == null);

        assertThat(result)
                .extracting(ClientEventEntity::getEventTypeId)
                .containsExactlyInAnyOrder(1L, 2L);
    }

    @Test
    void mapToClientEventEntities_should_handle_empty_list() {
        // Given
        UUID subscriptionId = UUID.randomUUID();
        Set<EventTypeEntity> eventTypeEntities = Set.of();

        // When
        List<ClientEventEntity> result = clientEventMapper.mapToClientEventEntityList(subscriptionId, eventTypeEntities);

        // Then
        assertThat(result).isNotNull();
        assertThat(result).isEmpty();
    }

    @Test
    void createClientEventEntities_should_use_default_method_to_create_entities() {
        // Given
        UUID subscriptionId = UUID.randomUUID();
        Set<EventTypeEntity> eventTypeEntities = Set.of(
                EventTypeEntity.builder()
                        .id(5L)
                        .eventName("TEST_EVENT")
                        .displayName("Test Event")
                        .category("TEST")
                        .build()
        );

        // When
        List<ClientEventEntity> result = clientEventMapper.mapToClientEventEntityList(subscriptionId, eventTypeEntities);

        // Then
        assertThat(result).hasSize(1);
        assertThat(result.getFirst().getId()).isNull();
        assertThat(result.getFirst().getSubscriptionId()).isEqualTo(subscriptionId);
        assertThat(result.getFirst().getEventTypeId()).isEqualTo(5L);
    }
}
